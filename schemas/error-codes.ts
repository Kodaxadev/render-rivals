type Values<T> = T[keyof T];
type ArrayValue<T> = T extends readonly (infer U)[] ? U : never;

export type ErrorCode = ArrayValue<Values<typeof ERROR_CODES>>;

export const ALL_ERROR_CODES: readonly ErrorCode[] = Object.freeze(
  Object.values(ERROR_CODES).flat() as ErrorCode[],
);

export function isErrorCode(value: string): value is ErrorCode {
  return (ALL_ERROR_CODES as readonly string[]).includes(value);
}
